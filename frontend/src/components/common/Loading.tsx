interface LoadingProps {
  size?: "sm" | "md" | "lg";
  className?: string;
}

const sizes = {
  sm: "w-4 h-4",
  md: "w-8 h-8",
  lg: "w-12 h-12",
};

export const Loading = ({ size = "md", className = "" }: LoadingProps) => {
  return (
    <div
      className={`
        ${sizes[size]}
        border-2 border-gray-200 border-t-blue-600
        rounded-full animate-spin
        ${className}
      `}
      role="status"
      aria-label="読み込み中"
    />
  );
};
